interface Move extends RawMove {
  index: number;
}

export function getMoveList(generation: number): Move[] {
  let moves: { [key: number]: { name: string; maxPP: number } } = {};

  for (let i = 0; i < generation; i += 1) {
    moves = { ...moves, ...moveList[i] };
  }

  return Object.entries(moves).reduce(
    (moves: Move[], [key, { name, maxPP }]) => {
      moves.push({ index: parseInt(key), name, maxPP });
      return moves;
    },
    [],
  );
}
