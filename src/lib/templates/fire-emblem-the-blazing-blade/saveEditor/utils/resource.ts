interface Character {
  index: number;
  name: string;
  conversations: {
    index: number;
    character: string;
    level: number;
  }[];
}

export const characterSupports: Character[] = [];

supportList.forEach((support) => {
  const character = characterList.find(
    (character) => character.index === support.character,
  );

  if (character) {
    const characterSupport: Character = {
      index: character.index,
      name: character.name,
      conversations: [],
    };

    supportConversationList.forEach((conversation) => {
      if (conversation.characters.includes(character.index)) {
        const supportCharacterIndex = conversation.characters.find(
          (support) => support !== character.index,
        );

        const supportCharacter = characterList.find(
          (character) => character.index === supportCharacterIndex,
        );

        if (supportCharacter) {
          characterSupport.conversations.push({
            index: conversation.index,
            character: supportCharacter.name,
            level: conversation.levels,
          });
        }
      }
    });

    characterSupports.push(characterSupport);
  }
});
