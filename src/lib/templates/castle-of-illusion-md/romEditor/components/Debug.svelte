  function decompressNemesisData(
    offset: number,
    size: number,
    xor: boolean,
  ): number[] {
    function setLookupTable(offset: number, value: number): void {
      const high = value >> 8;
      const low = value - (high << 8);

      lookupTable[offset] = high;
      lookupTable[offset + 0x1] = low;
    }

    function pushBuffer(value: number): void {
      buffer.push(
        (value >> 24) & 0xff,
        (value & 0xff0000) >> 16,
        (value & 0xff00) >> 8,
        value & 0xff,
      );
    }

    const lookupTable: number[] = [];
    const buffer: number[] = [];

    let v1 = getInt(offset, "uint8");
    let v2 = 0x0;
    let v3 = 0x0;
    let v4 = 0x0;
    let v5 = 0x0;
    let v6 = offset + 0x1;
    let v7 = 0x0;

    while (v1 !== 0xff) {
      v7 = v6;
      v3 = v1;

      while (true) {
        v6 = v7 + 0x1;
        v1 = getInt(v7, "uint8");

        if (v1 > 0x7f) {
          break;
        }

        v3 = ((v1 & 0xf) << 8) | (v1 & 0x70) | (v3 & 0xf);
        v2 = 8 - (v1 & 0xf);

        v7 += 0x2;

        if (v2 == 0x0) {
          setLookupTable(getInt(v6, "uint8") * 2, v3);
        } else {
          v4 = (getInt(v6, "uint8") << (v2 & 0x3f)) * 2;
          v5 = (1 << (v2 & 0x3f)) - 1;

          while (v5 !== -1) {
            setLookupTable(v4, v3);
            v4 += 0x2;
            v5 -= 1;
          }
        }
      }
    }

    let a5 = size;
    let d2 = 0x0;
    let d4 = 0x0;
    let d5 = getInt(v6, "uint16", { bigEndian: true });

    // Constants
    let d3 = 0x8;
    let d6 = 0x10;

    let var2 = 0x0;
    let var3 = 0x0;

    let end = false;

    while (!end) {
      var3 = (d5 >> (d6 - 0x8)) & 0xff;

      if (var3 < 0xfc) {
        var3 = var3 * 2;
        d6 -= lookupTable[var3];

        if (d6 <= 0x8) {
          d6 += 0x8;
          v6 += 0x1;
          d5 = getInt(v6, "uint16", { bigEndian: true });
        }

        var3 = ((lookupTable[var3] << 8) + lookupTable[var3 + 0x1]) & 0xff;
        var2 = var3;
        var3 &= 0xf;
        var2 = (var2 & 0xf0) >> 4;
      } else {
        d6 -= 0x6;

        if (d6 <= 0x8) {
          d6 += 0x8;
          v6 += 0x1;
          d5 = getInt(v6, "uint16", { bigEndian: true });
        }

        d6 -= 0x7;
        var3 = d5 >> d6;
        var2 = var3;
        var3 &= 0xf;
        var2 &= 0x70;

        if (d6 <= 0x8) {
          d6 += 0x8;
          v6 += 0x1;
          d5 = getInt(v6, "uint16", { bigEndian: true });
        }

        var2 >>= 4;
      }

      while (var2 >= 0x0) {
        d4 = (d4 << 4) | var3;
        d3 -= 0x1;

        if (d3 === 0x0) {
          let value = d4;

          if (xor) {
            d2 ^= d4;

            value = d2;
          }

          pushBuffer(value);

          a5 -= 0x1;

          if (a5 > 0x0) {
            d4 = 0x0;
            d3 = 0x8;
          } else {
            end = true;
          }
        }

        var2 -= 0x1;
      }
    }

    return buffer;
  }

