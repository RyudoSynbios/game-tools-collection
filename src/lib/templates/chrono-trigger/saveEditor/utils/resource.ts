export const itemsGroups: ResourceGroups = itemTypes.map((type) => ({
  name: type.name,
  options: [],
}));
export const weaponsGroups: ResourceGroups = itemTypes
  .filter((type) => type.index <= 0x6)
  .map((type) => ({
    name: type.name,
    options: [],
  }));

inventory.forEach((item) => {
  items[item.index] = item.name;
  itemsGroups[item.type].options.push(item.index);

  if (item.type <= 0x6) {
    weapons[item.index] = item.name;
    weaponsGroups[item.type].options.push(item.index);
  } else {
    switch (item.type) {
      case 0x7:
        armors[item.index] = item.name;
        break;
      case 0x8:
        helmets[item.index] = item.name;
        break;
      case 0x9:
        accessories[item.index] = item.name;
        break;
    }
  }
});

