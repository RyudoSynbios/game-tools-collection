export const items: Resource = {};

export const itemsGroups: ResourceGroups = itemTypes.map((type) => ({
  name: type.name,
  options: [],
}));

export const itemsOrder: number[] = [0];

itemList.forEach((item) => {
  const index = item.type | (item.subtype << 0x8);

  items[index] = item.name;
  itemsOrder.push(index);

  if (isInRange(item.type, 0x20, 0x3f)) {
    itemsGroups[0].options.push(index);
  } else if (isInRange(item.type, 0x41, 0x43)) {
    itemsGroups[1].options.push(index);
  } else if (isInRange(item.type, 0x45, 0x4e)) {
    itemsGroups[2].options.push(index);
  } else if (isInRange(item.type, 0x4f, 0x50)) {
    itemsGroups[3].options.push(index);
  } else if (isInRange(item.type, 0x51, 0x56)) {
    itemsGroups[item.type - 0x4d].options.push(index);
  } else if (isInRange(item.type, 0x57, 0x60)) {
    itemsGroups[0xa].options.push(index);
  }
});
